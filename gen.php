$dir = opendir('items');

$template = file_get_contents("base.html");

$menuTMP = null;
while ( ( $file = readdir ( $dir ) ) !== false )
{
	if ( substr($file,0,1) != '.' )
	{
		$menu = substr($file, 0, -5);
		$menuTMP .= "<li><h7><a href=\"{$menu}.html\">{$menu}</a></h7></li>";
	}
}

$dir = opendir('items');
while ( ( $file = readdir ( $dir ) ) !== false )
{
	if ($file != '.' && $file != '..')
	{
		echo "Generating " . $file . "\r\n";

		$open = file_get_contents('items/' . $file);

		$replace = $template;
		$replace = preg_replace("#{CONTENT}#", $open, $replace);
		$replace = preg_replace("#{MENU}#", $menuTMP, $replace);

		$file = fopen($file, 'w');
		@fwrite($file, $replace);
		@fclose($file);
	}
}

